package com.ust.examples;

/**
 * Task1
 * Switch case
 *
 */
 public class Example12 {

	
	public static void main(String[] args) {
		char ch='b';
		switch(ch) {
		case 'd':
		System.out.println("Case1");
		break;
		case 'b':
			System.out.println("Case2");
			break;
		case 'x':
			System.out.println("Case3");
			break;
		default:
			System.out.println("default");
			break;
		}

	}

}
